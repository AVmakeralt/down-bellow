/*
 * ENEMY: Void Crawler
 * -------------------
 * A small corrupted shade, low to the ground, four legs, one glowing eye,
 * blood-red mouth. Native to the Ash-Foot Town outskirts.
 *
 * 32x32 grid sprite, hand-authored. One enemy per file (per project spec).
 *
 * Palette:
 *   1 = void black (legs / outline)
 *   7 = void purple (body)
 *   8 = void purple bright (eye)
 *   e = blood red (mouth / claws when attacking)
 *   f = voidscrew glow (parryable claw telegraph)
 *
 * Frames: idle, walk1, walk2, attack, hurt
 *
 * Combat notes (from design doc):
 *   - The 'attack' frame is the telegraph. The claw hitbox is parryable.
 *   - On a successful parry (player's voidslash hits this claw), the
 *     crawler's attack freezes for 20 frames, leaving the hitbox
 *     vulnerable to a follow-up.
 *   - The 'hurt' frame plays during the post-parry stun window.
 */

